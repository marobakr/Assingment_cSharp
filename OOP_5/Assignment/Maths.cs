namespace Assignment;

static class Maths
{
  public static int Add(int X, int Y)
     {
         return X + Y;
     }
     public static int Subtract(int X, int Y)
     {
         return X - Y;
     }
     public static int Multiply(int X, int Y)
     {
         return X * Y;
     }
     public  static int Divide(int X, int Y)
     {
         return X / Y;
     }
}